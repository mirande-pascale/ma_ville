        <h1>conseils de voyages et culturel</h1>
        <p>Ces derniers temps, « voyage responsable » et « tourisme durable » sont devenus des mots clés.
           À mesure que le tourisme se développe, ses effets sur l’environnement et les communautés locales peuvent devenir un problème. 
           Il est donc demandé aux voyageurs de se comporter de manière réfléchie. Comprendre la culture japonaise et l’environnement de Tokyo vous aidera à être un voyageur responsable. 
           Votre comportement contribuera à l’avenir des séjours et du tourisme durables et responsables au Japon.</p>
        <h1>quel sont les facon d etre un tourist responsable???</h1>
        <p>Voici six façons, selon le site du tourisme de l’ONU (« Pour un tourisme plus durable » et « Conseils pour être un voyageur responsable »), 
          permettant d’être un touriste responsable à Tokyo.</p>
